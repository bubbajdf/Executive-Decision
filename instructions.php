<h2>SETUP</h2>
<div>
Players decide how many months will consitute a game.  Generally a game consists
of 12 months, however, beginners may prefer a shorter game until they become
familar with the play.
<br><br>
Each player receives a Tally sheet and the following amount of money:
<ul>
<li>2 players: $900</li>
<li>3 players: $600</li>
<li>4 players: $450</li>
<li>5 players: $350</li>
<li>6 players: $300</li>
</ul>
One player is chosen to be the Banker throughout the game.  That player handles
the Raw Material Certificates and money and posts the current Market Prices on the
Price Level Board.
</div>
<h2>PLAY</h2>
<div>
Each month of play consists of:
<ol>
<li>Purchasing Raw Materials, which are used to</li>
<li>Manufactoring the products of your choice and</li>
<li>Selling the Finished Goods in an attempt to make the greatest profit for your corporation.</li>
</ol>
<b>MONTH 1 (January) consists of the following, in order:</b><br><br>
