<h3>2. MANUFACTORING.</h3>
<div>
After consulting the Productions Chart on the Price 
Level Board, each player secretly decides on the amounts and kinds of Finished
Goods he <i>intends to manufacture</i> from the Raw Materials he has on hand.
<br><br>
Three units of Raw Materials are required to manufacture one unit of Finished
Goods as shown on Production Chart.  If a player is unable to supply the Raw
Materials specified on the Chart, he may still manufacture his Goods by
<i>substituting</i> an equal number of units of another Raw Material.  He
may either substitute X-FINE for FINE or FINE for STANDARD; these are the
only substitutions permitted.
<br><br>
<i>Example: Player wishes to manufacture a unit of "C".  Since he has only 1
unit of STANDARD he substitutes 1 unit of FINE.  Thus, his "C" will consist
of 2 FINE + 1 STANDARD.</i>
<br><br>
</div>
<h3>3. SELLING THE FINISHED GOODS</h3>
<div>
After consulting the Price Level Board
(<i>Opening Price</i> column and <i>Market Calculator</i>), each player enters
his January offering of Finished Goods in the <i>Bid</i> columns of his Tally
sheet - secretly recording <i>how many units</i> of each of the Finished Goods
he wishes to sell and the <i>price per unit</i> he is willing to accept.  He
may offer as many units of Finished Goods as he is able to manufacture.
<br><br>
After all players have entered their bids, Market Prices for the Finished Goods 
are determined in the same manner as for <i>Purchasing Raw Material Certificates</i>
except that Finished Goods section of Market Calculator is used.
<br><br>
<i>Example: In January, offerings of "A" total 7 units.  Market Calculator shows -3;
since Opening Price of "A" is $140, January Market Price is $137.  No units of "B"
were offered; Calculator shows +11 which is added to Opening Price of $115 for 
January Market Price of $126.  Offerings of "C" totaled 5 units. Since Calculator
shows +1 and Opening Price of "C" is $90, January Market Price is $91.</i>
<br><br>
